/**
 * Mostra i numeri da 1 a 100 in terminale
 * @author Luciano Faretra
 * @version 0.1
 */
public class counting {
    /**
     * Punto d'ingresso del programma
     * @param args Array contenente le informazioni passate da terminale al programma
     */
    public static void main(String[] args) {
        for(int i = 1; i<=100; i++){
            System.out.println(i);
        }

    }
}
